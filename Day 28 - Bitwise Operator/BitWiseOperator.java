public class BitWiseOperator
{
	public static void main(String[] args) {
		System.out.println(4&5); //bitwise AND operator 
		
		System.out.println(4|5); //bitwise OR operator
		
		System.out.println(4^5);  //bitwise xor operator
		
		System.out.println(~4); //bitwise not operator
		
		System.out.println(5<<4); // bitwise left shift operator 
		
		System.out.println(5>>4); //bitwise right shift operator
	}
}
